package com.kelompok4;

import static java.lang.Math.sqrt;

/**
 *
 * @author Faerul salamun 133040088
 */
public class Math {

    /**
     *
     * @param n1
     * @param n2
     * @return
     */
    public int mod(int n1, int n2) {
        return n1 % n2;
    }

    /**
     *
     * @param n
     * @return
     */
    public double sqrt(double n) {
        return java.lang.Math.sqrt(n);
    }
}
